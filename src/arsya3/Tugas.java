    private void switch_ThemeMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_switch_ThemeMouseReleased
        // TODO add your handling code here:
        if (background_toogle == "white") {
            bg = new ImageIcon(getClass().getResource("/src/Netbean B Form.jpg"));
            background.setIcon(bg);
            background_toogle = "black";
        }else if (background_toogle == "black") {
            bg = new ImageIcon(getClass().getResource("/src/Netbean W Form.jpg"));
            background.setIcon(bg);
            background_toogle = "white";
        }
        
    }//GEN-LAST:event_switch_ThemeMouseReleased

